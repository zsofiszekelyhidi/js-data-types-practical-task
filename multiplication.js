// string * boolean
let result = "2" * true;
console.log("'2' * true = " + result);

result = "2" * false;
console.log("'2' * false = " + result);

// string * number
result = "2" * 2;
console.log("'2' * 2 = " + result);

// number * boolean
result = 2 * true;  
console.log("2 * true = " + result);

result = 2 * false;  
console.log("2 * false = " + result);