// string + boolean
let sum = +"4" + true; 
console.log("'4' + true = " + sum);

sum = Number("4") + false; 
console.log("'4' + false = " + sum);

// string + number
sum = +'7' + 4  
console.log("'7' + 4 = " + sum);

// number + boolean
sum = 6 + true
console.log("6 + true = " + sum);

sum = 6 + false
console.log("6 + false = " + sum);
